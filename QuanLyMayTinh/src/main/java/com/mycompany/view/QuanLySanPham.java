    private void btnSuaSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaSanPhamActionPerformed
        // TODO add your handling code here:
        JTable table = controller.getTable();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int Row = controller.getRowSua();
        Row = table.convertColumnIndexToModel(Row);
        
        String sql_XOA = "DELETE FROM dbo.SanPham  " + "WHERE ma_may_tinh = ?";
        String sql_Them = "INSERT INTO dbo.SanPham(ma_may_tinh,ten_may_tinh,xuat_xu,thoi_gian_bao_hanh,so_luong,gia_ban)    " + " VALUES(?, ?, ?, ? , ? ,  ? ) ";
        try (Connection c = DBConnect.getConnection();
        PreparedStatement ps_Xoa = c.prepareCall(sql_XOA);
        PreparedStatement ps_Them = c.prepareCall(sql_Them);)
        {
            ps_Xoa.setString(1, model.getValueAt(Row, 1).toString());
            if( ThemSanPham(1) == 1)
            {
                ps_Xoa.executeUpdate();
                ps_Them.setString(1, txtMaMayTinh.getText());
                ps_Them.setString(2, txtTenMayTinh.getText());
                ps_Them.setString(3,txtXuatXu.getText());
                ps_Them.setString(4, jcbThoiGianBaoHanh.getSelectedItem().toString());
                ps_Them.setInt(5, Integer.valueOf(txtSoLuong.getText()) );
                ps_Them.setInt(6, Integer.valueOf(txtGiaBan.getText()) );
                ps_Them.executeUpdate();
            }
            
        } catch (Exception ex) {System.out.println(ex.getMessage());}
    }//GEN-LAST:event_btnSuaSanPhamActionPerformed

