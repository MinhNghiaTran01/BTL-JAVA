    private void btnTimKiemSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemSanPhamActionPerformed
        // TODO add your handling code here:
        List<MayTinh> listItem = controller.getListMayTinh();
        List<MayTinh> SearchListItem = new ArrayList<>();
        JTable table = controller.getTable();
        
        for(int i=0;i<listItem.size();i++)
        {
            if(txtTimKiemXuatXu.getText().length()>0&&listItem.get(i).getXuat_xu().equalsIgnoreCase( txtTimKiemXuatXu.getText() )== false) continue;
            if(txtTimKiemMMT.getText().length()>0&&listItem.get(i).getMa_may_tinh().equalsIgnoreCase( txtTimKiemMMT.getText() )== false) continue;
            if(txtTimKiemTMT.getText().length()>0&&listItem.get(i).getTen_may_tinh().equalsIgnoreCase( txtTimKiemTMT.getText() )== false) continue;
            if(txtTimKiemGiaTu.getText().length()>0 )
            {
                int Gia_tu = Integer.valueOf(txtTimKiemGiaTu.getText());
                if(listItem.get(i).getGia_ban()<Gia_tu ) continue;
            }
            
            if(txtTimKiemGiaDen.getText().length()>0 )
            {
                int Gia_den = Integer.valueOf(txtTimKiemGiaDen.getText());
                if(listItem.get(i).getGia_ban()>Gia_den ) continue;
            }
            
            
            
            if(txtTimKiemSoLuongTu.getText().length()>0 )
            {
                int SO_LUONG_TU = Integer.valueOf(txtTimKiemSoLuongTu.getText());
                if(listItem.get(i).getSo_luong() < SO_LUONG_TU ) continue;
            }
            
            if(txtTimKiemSoLuongDen.getText().length()>0 )
            {
                int SO_LUONG_DEN = Integer.valueOf(txtTimKiemSoLuongDen.getText());
                if(listItem.get(i).getSo_luong()> SO_LUONG_DEN ) continue;
            }
            SearchListItem.add(listItem.get(i));
        }
        
        ClassTableModel ctm = new ClassTableModel();
        String [] CLOUMNS = controller.getColumnName();
        DefaultTableModel model = ctm.setTableMayTinh(SearchListItem, CLOUMNS );
        table.setModel(model);
        DefaultTableCellRenderer centerrender = new DefaultTableCellRenderer();

        centerrender.setHorizontalAlignment( JLabel.CENTER );
        
        table.getColumnModel().getColumn(0).setCellRenderer( centerrender );
        
        DefaultTableCellRenderer rightrender = new DefaultTableCellRenderer();
        rightrender.setHorizontalAlignment( JLabel.LEFT );
        table.getColumnModel().getColumn(1).setCellRenderer( rightrender );
        table.getColumnModel().getColumn(2).setCellRenderer( rightrender );
        table.getColumnModel().getColumn(3).setCellRenderer( rightrender );
        table.getColumnModel().getColumn(4).setCellRenderer( rightrender );
        table.getColumnModel().getColumn(5).setCellRenderer( rightrender );
        table.getColumnModel().getColumn(6).setCellRenderer( rightrender );
        
        JScrollPane scroll = new JScrollPane();
        scroll.getViewport().add(table);
        scroll.setPreferredSize(new Dimension(1350,330));
        jpnTableQLSP.removeAll();
        jpnTableQLSP.setLayout(new BorderLayout());
        jpnTableQLSP.add(scroll);
        jpnTableQLSP.validate();
        jpnTableQLSP.repaint();
        
    }//GEN-LAST:event_btnTimKiemSanPhamActionPerformed

    private void XoaSanPham(){
        try {
            String errors = "";
            if(txtMaMayTinh.getText().equals(""))
            {
                errors += "Mã máy tính chưa được nhập\n";
                txtMaMayTinh.setBackground(Color.red);
            }
            else    txtMaMayTinh.setBackground(Color.WHITE);
           
            
            if(txtTenMayTinh.getText().equals(""))
            {
                errors+= "Tên máy tính chưa được nhập\n";
                txtTenMayTinh.setBackground(Color.red);
            }
            else txtTenMayTinh.setBackground(Color.white);
            
            String Item = (String) jcbThoiGianBaoHanh.getSelectedItem();
            if(Item.length()==1)
            {
                errors+= "Thời gian bảo hành chưa được nhập\n";
                jcbThoiGianBaoHanh.setBackground(Color.red);
            }
            else jcbThoiGianBaoHanh.setBackground(Color.white);
            
            if(txtXuatXu.getText().equals(""))
            {
                errors+= "Xuất xứ chưa được nhập\n";
                txtXuatXu.setBackground(Color.red);
            }
            else txtXuatXu.setBackground(Color.white);
            
            if(txtGiaBan.getText().equals(""))
            {
                errors+= "Giá bán chưa được nhập\n";
                txtGiaBan.setBackground(Color.red);
            }
            else txtGiaBan.setBackground(Color.white);
            
            if(txtSoLuong.getText().equals(""))
            {
                errors+= "Số lượng chưa được nhập";
                txtSoLuong.setBackground(Color.red);
            }
            else txtSoLuong.setBackground(Color.white);
            
            if(!errors.isEmpty())
            {
                JOptionPane.showMessageDialog(this, errors);
            }
            else
            {
                String sql = "DELETE FROM dbo.SanPham  " + "WHERE ma_may_tinh = ?";
                try (Connection c = DBConnect.getConnection();
                PreparedStatement ps = c.prepareCall(sql);)
                {
                    ps.setString(1, txtMaMayTinh.getText());
                    
                    MayTinhDAO mtd = new MayTinhDAOImpl();
                    List<MayTinh> list_may_tinh = mtd.getList();
                    boolean check = false;
                    for(int i=0;i<list_may_tinh.size();i++)
                    {
                        if(list_may_tinh.get(i).getMa_may_tinh().equals(txtMaMayTinh.getText()))
                        {
                            check = true;
                            break;
                        }
                    }
                    
                    if(check==true)
                    {
                        ps.executeUpdate();
                        JOptionPane.showMessageDialog(this,"Đã XÓA thành công");
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this,"Mã máy tính không có trong cơ sở dữ liệu");
                    }
                    
                } catch (Exception ex) {System.out.println(ex.getMessage());}
            }
        } 
        catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Erorr " + e.getMessage());
        }
    }
